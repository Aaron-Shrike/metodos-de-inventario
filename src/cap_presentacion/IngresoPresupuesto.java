    private void cajaCantidadKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cajaCantidadKeyReleased
        boolean letra = false;

        if(cajaCantidad.getCaretPosition() == 0 && cajaCantidad.getText().equals("")){

            cajaCantidad.setText("Ejm: 2500");
            cajaCantidad.setForeground(new Color(153,153,153));
            cajaCantidad.setCaretPosition(0);
        }else{

            for (int i = 0; i < cajaCantidad.getText().length(); i++) {
                char c = cajaCantidad.getText().charAt(i);

                if(c != '1' && c != '2' && c != '3' && c != '4' && c != '5'
                    && c != '6' && c != '7' && c != '8' && c != '9' && c != '0'){

                    letra = true;
                }
            }

            if(letra == true){
                cajaCantidad.setForeground(new Color(255,51,51)); // rojo
            }else{
                cajaCantidad.setForeground(new Color(0,0,0)); // negro
            }
        }
    }//GEN-LAST:event_cajaCantidadKeyReleased

