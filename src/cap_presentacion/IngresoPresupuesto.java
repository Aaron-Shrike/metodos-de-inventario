    private void cajaAñoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cajaAñoKeyReleased
        // TODO add your handling code here:
        boolean letra = false;

        if(cajaAño.getCaretPosition() == 0 && cajaAño.getText().equals("")){

            cajaAño.setText("Ejm: 2019");
            cajaAño.setForeground(new Color(153,153,153));
            cajaAño.setCaretPosition(0);
        }else{

            for (int i = 0; i < cajaAño.getText().length(); i++) {
                char c = cajaAño.getText().charAt(i);

                if(c != '1' && c != '2' && c != '3' && c != '4' && c != '5'
                    && c != '6' && c != '7' && c != '8' && c != '9' && c != '0'){

                    letra = true;
                }
            }

            if(letra == true){
                cajaAño.setForeground(new Color(255,51,51)); // rojo
            }else{
                cajaAño.setForeground(new Color(0,0,0)); // negro
            }
        }
    }//GEN-LAST:event_cajaAñoKeyReleased

